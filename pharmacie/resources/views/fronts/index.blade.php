            <div class="button-14">
              <div class="text-wrapper-168">Overview</div>
              <img class="caret-down-svg-3" src="img/image.svg" />
              <p class="male-pattern">
                Male pattern baldness (MPB) is common – and probably more common than you think. 30% of men aged 30 have
                it to some<br />degree. By the age of 70, that proportion is 70%. While some of us can even see it in
                our 20s, others can hold out until their older<br />years. The chances are that you will almost
                certainly see some degree of thinning.
              </p>
              <p class="text-wrapper-169">
                But what are the common causes of hair loss? And is it treatable? Read on to find out more.
              </p>
            </div>
            <div class="button-15">
              <p class="text-wrapper-170">The Causes of MPB and Other Hair Loss</p>
              <img class="caret-down-svg-3" src="img/caret-down-svg.svg" />
              <p class="in-of-cases">
                In 90% of cases, though, male hair loss is caused by MPB (also known as androgenic alopecia). This is a
                result of your hormones<br />and genes – and your follicles’ sensitivity to dihydrotestosterone (DHT).
                DHT is a male hormone that gives your male<br />characteristics when young, but it impacts hair growth
                as you age.
              </p>
              <p class="DHT-binds-to-hair">
                DHT binds to hair follicles on your scalp, and causes them to shrink, weaken, and stop producing healthy
                hair. It also affects your<br />hair growth cycle, meaning that your follicles are in their resting
                phase for longer and stay in the growth phase (or anagen phase)<br />for a shorter time. MPB is
                treatable and even reversible.
              </p>
              <p class="there-are-other">
                There are other reasons why you can lose your hair – and, unfortunately, not all of them are treatable.
                Alopecia areata is one<br />possibility, caused by autoimmune disease, in which hair falls out in
                clumps. Another cause is traction alopecia, where hair falls<br />out due to tension at the root. This
                is usually the result of wearing a tight-fitting helmet for too long, or wearing hairstyles such as<br />braids,
                cornrows, or hairpieces too tightly. It’s usually accompanied by itchiness and redness on the scalp.
              </p>
              <p class="trichotillomania-or">
                Trichotillomania, or obsessive hair pulling, is another of the types of hair loss, caused by physically
                pulling out hair from the scalp<br />or eyebrows. Meanwhile, telogen effluvium is the name of hair loss
                caused by stress. Other medical conditions and treatments –<br />including fungal infection and
                chemotherapy – can also cause hair loss. Consult your GP or a dermatologist before treatment.
              </p>
            </div>
            <div class="button-16">
              <p class="text-wrapper-171">Is my hair loss treatable?</p>
              <img class="caret-down-svg-3" src="img/caret-down-svg-6.svg" />
              <p class="if-you-re-male-and">
                If you’re male and in the 90% of hair loss cases, take a deep breath. It’s okay. MPB is easily treated
                using widely approved<br />treatments.
              </p>
              <p class="you-ll-be-able-to">
                You’ll be able to recognise if you have MPB. It first affects either the hair above your temples or on
                your crown. You’ll see your<br />hairline receding into a “widow’s peak”, a v-shaped hairline at the
                front of your head. Elsewhere, thinning hair will be seen across<br />the top of the head. Importantly,
                it won’t fall out suddenly or in clumps, and it won’t leave behind a rash.
              </p>
              <p class="there-are-two">
                <span class="text-wrapper-172"
                  >There are two effective and approved treatments for a receding hairline. Known as </span
                >
                <span class="text-wrapper-26">Finasteride</span>
                <span class="text-wrapper-172"> (often sold as Propecia) and <br /></span>
                <span class="text-wrapper-26">Minoxidil</span>
                <span class="text-wrapper-172"> (Rogaine or Regaine).</span>
              </p>
              <p class="the-first">
                The first, Finasteride, tackles the root cause of MPB by blocking the production of the hormone DHT.
                Effective in 90% of men<br />and usually taken orally as a daily pill, Finasteride intervenes to prevent
                the shrinkage of hair follicles before they become<br />permanently damaged.
              </p>
              <p class="minoxidil-meanwhile">
                Minoxidil, meanwhile, is applied topically as a spray. It’s a “vasodilator”, widening blood vessels on
                the scalp to facilitate the<br />transport of nutrients and oxygen. Use the spray twice daily – and it’s
                shown to help regrowth in two thirds of men. We<br />recommend that men use both of these treatments
                together.
              </p>
              <p class="strong-note">
                <span class="text-wrapper-172"
                  >Note: Many men report that topical Minoxidil has not worked for them because they lack the SULT1A1
                  sulfotransferase enzyme<br />in their hair follicles to activate the topical Minoxidil to the active
                  form Minoxidil Sulphate. Studies show </span
                >
                <span class="text-wrapper-26">oral Minoxidil</span>
                <span class="text-wrapper-172"
                  > (a tablet<br />form) is more effective as it utilises the sulfotransferase enzyme present in the
                  liver, and is not reliant on it being present in the<br />scalp.</span
                >
              </p>
            </div>
            <div class="button-17">
              <p class="text-wrapper-173">What are the side effects of the treatments?</p>
              <img class="caret-down-svg-3" src="img/caret-down-svg-4.svg" />
              <p class="finasteride-and">
                Finasteride and Minoxidil are licensed as effective treatments for hair loss. Like all medications, they
                can have side effects.<br />However, the good news is that these side effects are rare, and the vast
                majority of people do not experience them.
              </p>
              <p class="the-side-effects-of">
                <span class="text-wrapper-174">The </span>
                <span class="text-wrapper-26">side effects of Minoxidil</span>
                <span class="text-wrapper-174"
                  > usually come as a result of overapplication. They include dizziness and drowsiness – as the
                  increased<br />blood flow lowers your blood pressure slightly – as well as irritation in the area of
                  your scalp where it has been applied. Other side<br />effects may include hypertrichosis (abnormal
                  amount of hair growth), changes in hair colour, headaches, fast heartbeat, fluid<br />retention
                  (swelling) in the hands, feet or legs, shortness of breath, and low blood pressure. More serious side
                  effects – including<br />chest pain, heart palpitations, nausea, and changes to your vision – are very
                  rare.</span
                >
              </p>
              <p class="over-of-men">
                <span class="text-wrapper-175">Over 98% of men experienced no serious </span>
                <span class="text-wrapper-26">side effects of Finasteride</span>
                <span class="text-wrapper-175"
                  > in clinical trials. However, weight loss and weight gain, skin<br />rashes, tenderness around the
                  nipples or lumps, dizziness and weakness, erectile dysfunction, testicular pain, poor seminal<br />quality
                  / infertility, ejaculation disorders, decreased libido and changes in mood or depression have all been
                  reported as side<br />effects. If your partner is pregnant, conservative advice is to use a
                  condom.</span
                >
              </p>
              <p class="unreported-side">
                <span class="text-wrapper-175"
                  >Unreported side effects may also occur. Any questions, just reach out to us via direct messaging
                  through your account,<br />WhatsApp, phone or book </span
                >
                <span class="text-wrapper-26">via this link</span>
                <span class="text-wrapper-175"> to schedule a call with one of our clinicians.</span>
              </p>
            </div>
            <div class="button-18">
              <p class="text-wrapper-176">Why wouldn’t I have a hair transplant?</p>
              <img class="caret-down-svg-3" src="img/caret-down-svg-5.svg" />
              <p class="hair-transplants-are">
                Hair transplants are not quite as permanent as they may seem. As MPB is a progressive condition, there’s
                no guarantee that your<br />hair loss won’t continue post-op. And, as a result, you might find yourself
                saving up for a second or a third transplant.
              </p>
              <p class="hair-transplants-are-2">
                Hair transplants are also accompanied by treatment with Finasteride and Minoxidil. These drugs tackle
                the root cause of hair loss,<br />where hair transplants only manage the symptoms. You’re increasing the
                chances of further hair loss if you don’t follow surgery<br />with these treatments. You’re the best
                judge of what you need, but if you’re getting a transplant, you’ll be taking these<br />medications
                afterwards anyway.
              </p>
            </div>
            <div class="button-19">
              <p class="text-wrapper-177">Can a shampoo treat my hair loss?</p>
              <img class="caret-down-svg-3" src="img/caret-down-svg-3.svg" />
              <p class="yes-it-s-true">
                Yes, it’s true. Shampoos can be effectively used to treat hair loss. However, with many duff products on
                the market, it’s important<br />that you know what you are looking for.
              </p>
              <p class="manual-s-power">
                <span class="text-wrapper-178">Manual’s </span>
                <span class="text-wrapper-26">Power Shampoo</span>
                <span class="text-wrapper-178"
                  > includes ingredients known to encourage healthy hair. Caffeine is known to stimulate hair
                  follicle<br />activity. Biotin is an essential vitamin for hair health, as it plays a role in the
                  production of keratin, the protein of which hair is<br />made. Meanwhile, saw palmetto is thought to
                  reduce the amount of DHT in the body, too. But Finasteride and Minoxidil are the<br />only proven
                  treatments.</span
